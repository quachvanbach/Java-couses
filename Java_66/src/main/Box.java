package main;

public class Box {
	private int value;

	public Box(int value) {
		super();
		this.value = value;
	}

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(int value) {
		this.value = value;
	}



	
	
}
